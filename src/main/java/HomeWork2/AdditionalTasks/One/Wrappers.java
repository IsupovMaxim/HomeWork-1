//В классе Wrappers создать и проинициализирвоать по одному экземпляру оберток над примитивами
public class Wrappers {
    static int a = 10000;
    static short b = 100;
    static byte c = 10;
    static long d = 121324242000000L;
    static float e = 3.45f;
    static double f = 3.56;
    static boolean g = false;
    static char h = 'n';
   // double myD = 15.4;
    public static void main(String[] args) {
        Integer myIntenger = a;
        Short myShort = b;
        Byte myByte = c;
        Long myLong = d;
        Float myFloat = e;
        Double myDouble = f;
        Boolean myBolean = g;
        Character myCharacter = h;

        System.out.println(myIntenger);
        System.out.println(myShort);
        System.out.println(myByte);
        System.out.println(myLong);
        System.out.println(myFloat);
        System.out.println(myDouble);
        System.out.println(myBolean);
        System.out.println(myCharacter);










    }
}
