package HomeWork2.AdditionalTasks.One;
//В недавно созданном проекте создать класс, назвать Primitives
//В классе Primitives создать и проинициализировать переменные всех возможных примитивных типов
public class Primitives {
    public static void main(String[] args) {
        int a = 10000;
        short b = 100;
        byte c = 10;
        long d = 121324242000000L;
        float e = 3.45f;
        double f = 3.56;
        boolean g = false;
        char h = 'n';
        System.out.println(" a = " + a);
        System.out.println(" b = " + b);
        System.out.println(" c = " + c);
        System.out.println(" d = " + d);
        System.out.println(" e = " + e);
        System.out.println(" f = " + f);
        System.out.println(" g = " + g);
        System.out.println(" h = " + h);
    }
}

